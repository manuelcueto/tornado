package seminario.futbol.model;

public class Arbitro {

    private String email;
    private String nombre;
    private String telefono;

    public Arbitro() {

    }

    public Arbitro(String email, String nombre, String telefono) {
	super();
	this.email = email;
	this.nombre = nombre;
	this.telefono = telefono;
    }

}
